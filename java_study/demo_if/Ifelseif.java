package demo_if;

public class Ifelseif {
    public static void main(String[] args) {
        int time = 20;
        if (time < 20) {
            System.out.println("Good morning.");
        } else if (time < 20) {
            System.out.println("Good day.");
        } else {
            System.out.println("Good evening.");
        }
    }
}
